int	ft_chknum(char *str)
{
	int	sign;
	int	nbr;

	while (*str)
	{
		while (*str == ' ' || (*str >= 9 && *str <= 13))
			str++;
		while (*str == '-' || *str == '+')
		{
			if (*str++ == '-')
				sign *= -1;
			if (sign == -1)
				return (1);
		}
		while (*str >= '0' && *str <= '9')
		{
			str++;
		}
		return (0);
	}
	return (1);
}
