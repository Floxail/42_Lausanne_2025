char	*chk_dix(char n1, char n2)
{
	char	*s1;

	s1 = malloc(sizeof(char) * (3));
	s1[0] = n1;
	if (n1 == '1')
		s1[1] = n2;
	else
		s1[1] = '0';
	return (s1);
}
